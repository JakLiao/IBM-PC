;根据欧几里德辗转相除法求两个正整数M、N的最大公约数R
S1	SEGMENT
M	DW 	48
N	DW 	32
R	DW	?
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	MOV	AX,M
	MOV	BX,N
L1:	XOR	DX,DX
	DIV	BX
	CMP	DX,0
	JE	PRINT
	MOV	AX,BX
	MOV	BX,DX
	JMP	L1

PRINT:
	MOV	R,BX
	
	XOR CX,CX;十进制输出
	MOV AX,BX
L2:	XOR DX,DX
	MOV BX,10
	DIV	BX
	PUSH DX
	INC CX
	CMP AX,0
	JNE L2
	
L3:	POP DX
	ADD DL,30H
	MOV	AH,02H
	INT 21H
	LOOP L3
	
EXIT:	
	MOV	AH,4CH
	INT 21H
MAIN ENDP
S3	ENDS
	END	MAIN