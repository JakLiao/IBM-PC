;设在A、B 和D 单元中分别存放着个数。若三个数都不是0，则求出三个数的和并存 放在S单元中；若其中有一个数为0，则把其他两个单元也清零。请编写此程序
S1	SEGMENT
A	DB	0FFH
B	DB	20
C	DB	30
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS

	LEA	SI,A
	LEA	DI,B
	LEA	BX,C
	MOV AL,BYTE PTR[SI]
	CMP	AL,0
	JE	CLEAR
	MOV DL,BYTE PTR[DI]
	CMP	DL,0
	JE	CLEAR
	MOV CL,BYTE PTR[BX]
	CMP	CL,0
	JE	CLEAR
	JMP	AD
	
CLEAR:	MOV	BYTE PTR[SI],30H
	MOV	BYTE PTR[DI],30H
	MOV BYTE PTR[BX],30H
	JMP	EXIT
	
AD:	XOR CH,CH
	XOR	DH,DH
	XOR	AH,AH

	ADD	DX,AX
	ADD	DX,CX
	
	XOR CX,CX;十进制输出
	MOV AX,DX
L1:	XOR DX,DX
	MOV BX,10
	DIV	BX
	PUSH DX
	INC CX
	CMP AX,0
	JNE L1
	
L2:	POP DX
	ADD DL,30H
	MOV	AH,02H
	INT 21H
	LOOP L2
	
EXIT:	MOV	AH,4CH
	INT 	21H

MAIN	ENDP
	S3	ENDS
	END	MAIN