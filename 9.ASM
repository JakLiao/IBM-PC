;试编写一段程序，从键盘接收一个四位的十六进制数，并在终端上显示与它等值的 二进制数。 
S1	SEGMENT
DATA	DB	100H DUP(?)
CRLF	DB	0DH,0AH,24H
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	MOV CH,4
L0:	MOV AH,01H
	INT 21H
	CMP AL,0DH
	JE	EXIT
	CMP AL,30H
	JB	EXIT
	CMP	AL,39H
	JBE	L2
	CMP	AL,41H
	JB	EXIT
	CMP	AL,46H
	JBE	L1
	CMP	AL,61H
	JB	EXIT
	CMP	AL,66H
	JA	EXIT
	SUB	AL,20H
L1:	SUB	AL,7H
L2:	SUB AL,30H
	
	MOV DL,AL
	XOR DH,DH
	MOV AX,BX
	MOV CL,4
	SHL AX,CL
	ADD AX,DX
	MOV BX,AX;每次结果存入BX
	
	DEC CH
	JNE L0
	
	LEA DX,CRLF;换行
	MOV AH,09H
	INT 21H
	
	MOV CX,16
	MOV AH,02H
L3:	ROL BX,1
	MOV DL,BL
	AND DL,1
	ADD DL,30H
	INT 21H
	LOOP L3
	
	
EXIT:	MOV	AH,4CH
	INT 	21H

MAIN	ENDP
	S3	ENDS
	END	MAIN