;已知数组A 包含15个互不相等的整数，数组B 包含20个互不相等的整数。试编制一个程序，把既在A 中又在B 中出现的整数存放于数组C 中
S1	SEGMENT
A 	DB 13,26,38,62,23,1,32,21,15,57,74,78,72,2,9
B 	DB 2,5,3,7,1,8,64,58,73,49,58,27,59,44,13,33,99,66,44,9
C 	DB 15 DUP(?)
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS

	MOV CX,15
	XOR	SI,SI
	XOR	DI,DI
L1:	PUSH CX
	MOV DH,A[SI];从数组A取一个数
	XOR BX,BX
	MOV CX,20
L2:	MOV	CL,B[BX];和数组B中每个数比较
	CMP	DH,DL
	JE	L3;如果相等就放入数组C
	INC BX
	LOOP	L2
L3:	MOV	C[DI],DH;放入数组C
	INC SI
	INC DI
	POP CX
	LOOP	L1;循环判断
	
EXIT:	MOV	AH,4CH
	INT 	21H

MAIN	ENDP
	S3	ENDS
	END	MAIN