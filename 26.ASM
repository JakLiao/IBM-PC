;以下面形式定义一个长整数，其所占用字节数由N得到，比如128位的数 
;5746352413DE89674523BC9A78563412H定义成： 
;        NUM DB  12H,34H,56H,78H,9AH,0BCH,23H,45H 
;        DB    67H,89H,0DEH,13H,24H,35H,46H,57H 
;       N    DB  $-NUM 
;试编写一个程序，实现对N 字节的NUM 的值求补，并把求补后的值仍保存在NUM 中
S1	SEGMENT
NUM 	DB  12H,34H,56H,78H,9AH,0BCH,23H,45H 
        DB  67H,89H,0DEH,13H,24H,35H,46H,57H 
N    	DB  $-NUM 
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	MOV	CX,N
	LEA SI,NUM
LP1:NOT	BYTE PTR[SI]
	INC	SI
	LOOP	LP1
	MOV	CX,N
	LEA SI,NUM
	STC	
LP2:ADC BYTE PTR[SI],0
	INC SI
	LOOP LP2

EXIT:	MOV	AH,4CH
	INT 	21H
MAIN ENDP
S3	ENDS
	END	MAIN