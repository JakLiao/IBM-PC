;有一个首地址为mem 的100个字的数组，试编制程序删除数组中所有为零的项，并 将后续项向前压缩，最后将数组的剩余部分补上零
S1	SEGMENT
MEM	DW	10H,12H,0,40H,0,23H,42H,23H,0F3H,0A4H
LEN	EQU	($ - MEM)/2
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S4	SEGMENT
MEM2	DW	110H DUP(?)
S4 ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3,ES:S4
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV	AX,S4
	MOV	ES,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
START:
	LEA	SI,MEM
	LEA	DI,MEM2
	MOV	CX,LEN
	CLD;DF位置0
L1:	LODSW;从串取指令
	CMP	AX,0
	JE	L2
	STOSW;存入串指令
L2:	LOOP L1

EXIT:	MOV	AH,4CH
	INT 	21H
MAIN ENDP
S3	ENDS
	END	MAIN