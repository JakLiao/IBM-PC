;从键盘上输入一串字符（用回车键结束，使用10号功能调用。），将其中的小写英 文字母变换为大写英文字母，其他字符保持不变。然后将变换后的字符串显示出来
S1	SEGMENT
BUFF	DB	100,?,100H DUP(?)
CRLF	DB	0DH,0AH,24H
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	LEA DX,BUFF
	MOV	AH,0AH
	INT	21H
	LEA DX,CRLF
	MOV	AH,09H
	INT 21H
	LEA	SI,BUFF
	INC	SI
	MOV	CL,[SI]
	XOR	CH,CH
	CLD
	INC SI
NEXT:LODSB
	MOV	DL,AL
	CMP	DL,61H
	JB	PRINT
	CMP	DL,7AH
	JA	PRINT
	SUB	DL,20H
PRINT:	MOV	AH,02H
	INT	21H
	LOOP NEXT

EXIT:	MOV	AH,4CH
	INT 21H
MAIN ENDP
S3	ENDS
	END	MAIN