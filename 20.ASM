;设有10个学生的成绩分别是76，69，84，90，73，88，99，63，100和80 分。试 编制一个子程序统计60-69 分，70-79 分，80-89 分，90-99 分和100分的人数，并分别放 到S6,S7,S8,S9和S10 单元中
S1	SEGMENT
SCORE	DB	76,69,84,90,73,88,99,63,100,80
LEN		EQU	($-SCORE)
S6		DB	0
S7		DB	0
S8		DB	0
S9		DB	0
S10		DB	0
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	LEA SI,SCORE
	CLD
	MOV	CX,LEN
START:
	LODSB
	CMP	AL,60
	JB	NEXT
	CMP	AL,70
	JAE	SCORE70
	LEA	DI,S6
	JMP	SETSCORE
SCORE70:
	CMP	AL,80
	JAE	SCORE80
	LEA	DI,S7
	JMP	SETSCORE
SCORE80:
	CMP	AL,90
	JAE	SCORE90
	LEA	DI,S8
	JMP	SETSCORE
SCORE90:
	CMP	AL,100
	JE	SCORE100
	LEA	DI,S9
	JMP	SETSCORE
SCORE100:
	LEA	DI,S10
	
SETSCORE:
	INC	BYTE PTR[DI]
NEXT:LOOP START

EXIT:	MOV	AH,4CH
	INT 	21H
MAIN ENDP
S3	ENDS
	END	MAIN