;在ADDR单元中存放着数Y的地址，试编制一个程序把Y中1的个数存入COUNT单元中。 
S1	SEGMENT
Y	DW	1234H
ADDR	DW	Y
COUNT	DW	?
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	XOR	CX,CX
	MOV	BX,ADDR
	MOV	AX,[BX]
REPEAT:TEST	AX,0FFFFH
	JZ	EXIT
	JNS	SHIFT
	INC CX
SHIFT:SHL	AX,1
	JMP	REPEAT

EXIT:	
	MOV	COUNT,CX
	MOV	AH,4CH
	INT 21H
MAIN ENDP
S3	ENDS
	END	MAIN