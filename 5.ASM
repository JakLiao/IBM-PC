;编写程序，从键盘接收一个小写字母，然后找出它的前导字符和后续字符，再按顺序用大写字母显示这三个字符
S1	SEGMENT
DATA	DB	100H DUP(?)
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS

	MOV	AH,07H
	INT	21H
	
	DEC AL
	MOV	DL,AL
	MOV	AH,02H
	INT	21H
	INC AL
	MOV	DL,AL
	MOV	AH,02H
	INT	21H
	INC AL
	MOV	DL,AL
	MOV	AH,02H
	INT	21H
	
EXIT:	MOV	AH,4CH
	INT 	21H

MAIN	ENDP
	S3	ENDS
	END	MAIN