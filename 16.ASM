	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS

	MOV AX,ARRAY[0];字
	MOV BX,ARRAY[2]
	MOV CX,ARRAY[4]
	CMP	AX,BX
	JE	L1
	CMP BX,CX
