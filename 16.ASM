	CMP AX,CX
	JE 	PRINT1
PRINT0:	MOV DL,30H;输出0
	MOV AH,2
	INT 21H
	JMP EXIT
	
L1:	CMP	BX,CX
	JE	PRINT2
	JMP	PRINT1

PRINT1:	MOV DL,31H;输出1
