;试编写一段程序，要求在长度为100H字的数组中，找出大于42H 的无符号数的个数并存入地址UP 开始区域；找出小于42H的无符号数的个数并存入地址DOWN开始区域
S1	SEGMENT
DATA	DW	100H DUP(?)
LEN		EQU	($-DATA)/2
UP 		DW	?
DOWN 	DW	?
S1	ENDS

S2	SEGMENT   STACK
	DW   60H DUP(?)
TOS	LABEL  WORD
S2	ENDS

S3	SEGMENT
	ASSUME DS:S1,SS:S2,CS:S3
MAIN	PROC  FAR
	MOV AX,S1
	MOV DS,AX
	MOV AX,S2
	MOV SS,AX
	LEA SP,TOS
	
	LEA SI,DATA
	XOR BX,BX
	XOR DX,DX	
	MOV CX,LEN
L1:	MOV AX,[SI]
	CMP	AX,42H;与42h比较
	JA	CNT1;大于
	JB	CNT2;小于
	JMP	L2;等于
CNT1:	INC	BX;大于计数
	JMP	L2
CNT2:	INC	DX;小于计数
L2:	ADD SI,2
	LOOP L1
	
	MOV UP,BX
	MOV DOWN,DX
EXIT:	MOV	AH,4CH
	INT 	21H

MAIN	ENDP
	S3	ENDS
	END	MAIN